// Helper function to get all feedback messages
export const getAllFeedbacks = () => {
  return [...feedbackMessages.dental, ...feedbackMessages.physio];
};

// Helper function to get feedbacks by category
export const getFeedbacksByCategory = (category) => {
  if (category === 'all') {
    return getAllFeedbacks();
  }
  if (category === 'dental') {
    return feedbackMessages.dental;
  }
  if (category === 'physio') {
    return feedbackMessages.physio;
  }
  return [];
};

// Function to get a specific feedback message by ID
export const getFeedbackById = (id) => {
  const allFeedbacks = getAllFeedbacks();
  const index = parseInt(id, 10);
  if (index >= 0 && index < allFeedbacks.length) {
    return allFeedbacks[index];
  }
  return null;
};

// Function to get a random feedback message
export const getRandomFeedback = () => {
  const allFeedbacks = getAllFeedbacks();
  const randomIndex = Math.floor(Math.random() * allFeedbacks.length);
  return allFeedbacks[randomIndex];
};

// Total count of feedback messages
export const getTotalFeedbackCount = () => {
  return getAllFeedbacks().length;
};

// Get count by category
export const getCategoryCount = (category) => {
  if (category === 'dental') return feedbackMessages.dental.length;
  if (category === 'physio') return feedbackMessages.physio.length;
  return getTotalFeedbackCount();
};
